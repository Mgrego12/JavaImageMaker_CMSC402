 // use BufferedImage method to make 2d array and collect coordinates, set color
public BufferedImage makeImage(int[][] nameData){
	BufferedImage imageBuff = new BufferedImage(maxX, maxY,
			BufferedImage.TYPE_INT_RGB);
	for(int x = 0; x < maxX; x++){
		for(int y = 0; y < maxY; y++){
			int pixel = nameData[x][y];
			if(pixel == 0){
				pixel = Color.PINK.getRGB();
				}else{
					pixel = Color.MAGENTA.getRGB();
					}
			imageBuff.setRGB(x, y, pixel);
			}
		}
	return imageBuff;
	}
